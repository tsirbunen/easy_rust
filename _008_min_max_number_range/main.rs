fn main() {
    // Getting the smallest and largest numbers of number types:
    println!("The smallest i8 is {} = -128", i8::MIN);
    println!("The biggest i8 is {} = 127", i8::MAX);
    println!("The smallest u32 is {} = 0", u32::MIN);
    println!("The biggest u32 is {} = 4294967295", u32::MAX);
}
